    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // BOTON DE CONSULTAR
        String id = jList1.getSelectedValue();
        System.out.println(id);
        for (int i=0;i<this.casillero.size();i++){
            System.out.println("entro");
            if(casillero.get(i).usuario != null){
                if(this.casillero.get(i).usuario.identificacion.equals(id)){
                    jTextField1.setText(this.casillero.get(i).usuario.identificacion);
                    jTextField2.setText(this.casillero.get(i).usuario.nombre);
                    jTextField3.setText(this.casillero.get(i).usuario.correo);
                    jTextField4.setText(this.casillero.get(i).usuario.telefono);
                    jTextField5.setText(this.casillero.get(i).usuario.direccion);
                    jComboBox1.setSelectedItem(this.casillero.get(i).usuario.nivel);
                    jComboBox2.setSelectedItem(this.casillero.get(i).usuario.sexo);
                    
                    SimpleDateFormat formatoDelTexto;
                    formatoDelTexto = new SimpleDateFormat("yyyy/MM/dd");
                    Date fecha = null;
                    try {

                    fecha = formatoDelTexto.parse(this.casillero.get(i).usuario.fecha);

                    } catch (ParseException ex) {
                    }
                    jDateChooser1.setDate(fecha);
                }
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        // TODO add your handling code here:
        char limit =evt.getKeyChar();
        if(Character.isLetter(limit)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this,"Solo puede ingresar números");
            jTextField1.setCursor(null);
        }
        else if((int)evt.getKeyChar()>32 && (int)evt.getKeyChar()<=47 || (int)evt.getKeyChar()>=58 &&(int)evt.getKeyChar()<=64 ||(int)evt.getKeyChar()>=123 &&(int)evt.getKeyChar()<=126){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this,"Solo puede ingresar números");
            jTextField1.setCursor(null);
        }
    }//GEN-LAST:event_jTextField1KeyTyped

