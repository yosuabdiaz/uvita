    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // BOTON DE ACEPTAR
        try{
        String id = jTextField1.getText();
        String nombre = jTextField2.getText();
        String correo = jTextField3.getText();
        String telefono = jTextField4.getText();
        String direccion = jTextField5.getText();
        String sexo = jComboBox2.getSelectedItem().toString();
        String dia = Integer.toString(jDateChooser1.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = Integer.toString(jDateChooser1.getCalendar().get(Calendar.MONTH) + 1);
        String year = Integer.toString(jDateChooser1.getCalendar().get(Calendar.YEAR));
        String fecha = (year + "-" + mes+ "-" + dia);
        System.out.println(fecha);
        String nivel = jComboBox1.getSelectedItem().toString();
        System.out.println(fecha);
        String id1 = jList1.getSelectedValue();
        for (int i=0;i<this.casillero.size();i++){
            if(casillero.get(i).usuario != null){
                if(this.casillero.get(i).usuario.identificacion.equals(id1)){
                    this.casillero.get(i).usuario.setIdentificacion(id);
                    this.casillero.get(i).usuario.setNombre(nombre);
                    this.casillero.get(i).usuario.setCorreo(correo);
                    this.casillero.get(i).usuario.setTelefono(telefono);
                    this.casillero.get(i).usuario.setDireccion(direccion);
                    this.casillero.get(i).usuario.setSexo(sexo);
                    this.casillero.get(i).usuario.setFecha(fecha);
                    this.casillero.get(i).usuario.setNivel(nivel);
                }
            }
            
        }
        
        
        ArrayList<Casillero> casilleros = this.casillero;
        Administrar_clientes obj = new Administrar_clientes(casilleros);
        obj.setVisible(true);
        this.setVisible(false);
        }
        catch(Exception ee){
            showMessageDialog(null,"Llene todos los campos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

